#include <iostream>
using namespace std;
int main()
{
    int x = 5;
    cout << x << endl;
    cout << x++ << endl; // output 5 //post increment
    cout << x << endl;

    cout << endl;

    int y = 5;
    cout << y << endl;
    cout << ++y << endl; // output 6 //pre increment
    cout << y << endl;

    // same you can apply for decrement
}